var mongoose = require('mongoose');
var UserSchema = new mongoose.Schema({
  first_name: {
    type: String,
    required: true
  },
  last_name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    unique: true,
    required: true
  },
  password: {
    type: String,
    required: true
  },
  confirm_password: String
});
mongoose.model('User', UserSchema);
module.exports = mongoose.model('User');
